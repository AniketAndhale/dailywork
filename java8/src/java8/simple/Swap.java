package java8.simple;

public class Swap {
public static void main(String[] args) {
	int a=10;
	int b=112;
	System.out.println("Before swapping ");
	System.out.println(a);
	System.out.println(b);
	int temp = a;
	a=b;
	b=temp;
	System.out.println("Aftwr swapping");
	System.out.println(a);
	System.out.println(b);
	
}
}

